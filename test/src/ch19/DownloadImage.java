package ch19;

import java.io.FileOutputStream;
import java.io.InputStream;
import java.io.OutputStream;
import java.net.URL;

public class DownloadImage 
{
	public static void main(String[] args) throws Exception 
	{
		String website = "https://imgnews.pstatic.net/image/139/2022/05/20/0002167316_001_20220520134301320.jpg?type=w647";
		
		System.out.println("다운로드를 시작합니다.");
		URL url = new URL(website);	//url 생성
		
		byte[] buffer = new byte[2048];	//buffer용 바이트 배열[2mb]
		//			파일로 저장  <==	프로그램 <== 서버의 이미지 파일
		
		//try ~ with 문
		//try(리소스선언부) {  } catch(Ex e) {  }
		//finally 가 없어도 리소스를 자동으로 종료시킴
		try(InputStream in = url.openStream(); OutputStream out = new FileOutputStream("c:\\test\\test.jpg"))
		{
			int length = 0;
			//읽은 바이트수 = 스트림.read(버퍼)
			//더이상 읽을 내용이 없으면 -1
			while((length = in.read(buffer)) != -1)
			{
				System.out.println(length + "바이트 읽음.");
				//출력스트림.write(버퍼,시작인덱스,길이값)
				out.write(buffer,0,length);
			}
			System.out.println("다운로드가 완료되었습니다...");
		}
		catch (Exception e)
		{
			e.printStackTrace();
		}
	}
}
